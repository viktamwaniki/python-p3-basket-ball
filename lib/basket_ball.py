def average_rebounds_brands():
    # initialize a dic. to hold gameData return and an empty dic. to store average rebounds by a shoe brand 
    gameData = game_dict()
    brand_rebounds = {} 

    # use a for..in  loop to iterate of the gameData values 
    # team : home or away 
    for team in gameData.values():
    #    player in this case is a list item.
     for player in team['players']: 
        brand = player['shoe_brand']
        rebounds = player['rebounds_per_game']
        # we need to add the shoe brand with a value rebounds per game to our dictionary brand_rebounds 
        #   {  "Nike" : [] }
        # explict check 
        # defaultdict(list)
        if brand not in brand_rebounds:
        #    give the key value name 
        #    give the data type of that key 
            brand_rebounds[brand] = []
            brand_rebounds[brand].append(rebounds) 

    # we now loop inside the brand_rebounds dictionary get the items in the list and get the average rebounds 
    for brands, rebounds_list in brand_rebounds.items():
        #  get the average rebounds for the current brand by dividing the sum of rebounds in the list by the number of players 
        avg_rebounds = sum(rebounds_list) / len(rebounds_list)
        # print out the avg_rebounds 
        print(f"{brands}:{avg_rebounds:.2f}")


average_rebounds_brands()